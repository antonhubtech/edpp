    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableAddStudent.getSelectedRow();

    // Check if user selected a row
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete");
        return;
    }

    // Get student ID from column 0
    int studentId = Integer.parseInt(
        tableAddStudent.getValueAt(selectedRow, 0).toString()
    );

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete this student?",
        "Confirm Delete",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM accountStudents WHERE student_id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, studentId);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Student deleted successfully");

            // Refresh table
            loadStudentTable();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting student: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton4ActionPerformed

