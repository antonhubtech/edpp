    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtAddStudentId.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Required to add Student ID");
        return;
    }

    if (txtAddSection.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Required to add Section");
        return;
    }

    if (txtAddUsername.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Required to add Username");
        return;
    }

    if (txtAddPassword.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Required to add Password");
        return;
    }

    try {
        int studentId = Integer.parseInt(txtAddStudentId.getText().trim());
        String section = txtAddSection.getText().trim();
        String username = txtAddUsername.getText().trim();
        String password = txtAddPassword.getText().trim();

        String sql = "INSERT INTO accountStudents(student_id, section, username, password) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, studentId);
        pst.setString(2, section);
        pst.setString(3, username);
        pst.setString(4, password);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Student added successfully");

        loadStudentTable();

        // clear textfields after save
        txtAddStudentId.setText("");
        txtAddSection.setText("");
        txtAddUsername.setText("");
        txtAddPassword.setText("");

        // optional: put cursor back to Student ID
        txtAddStudentId.requestFocus();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Student ID must be a number");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error adding student: " + e.getMessage());
    }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

